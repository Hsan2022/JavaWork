package org.example.glab_303_3_1;
//The toLowerCase() method converts all the characters of the String to lowercase.

public class StringLowerExample {
    public static void main(String args[]){


        //declared string, some upper some lower case
        String s1="HELLO HOW Are You?";


        //reassigned no params needed
        String s1lower=s1.toLowerCase();


        //print new string value
        System.out.println(s1lower);} //=> hello how are you?
}
